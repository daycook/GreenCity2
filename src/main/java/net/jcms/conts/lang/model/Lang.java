package net.jcms.conts.lang.model;

import net.jcms.framework.base.model.BaseModel;
import net.jcms.framework.cd.model.CdDtl;

public class Lang extends BaseModel{

	private Long langId;
	private String langTp;
	private String dispNm;
	private Boolean basicYn;
	
	private CdDtl langTpCd;
	
	public Boolean getBasicYn() {
		return basicYn;
	}
	public void setBasicYn(Boolean basicYn) {
		this.basicYn = basicYn;
	}
	public CdDtl getLangTpCd() {
		return langTpCd;
	}
	public void setLangTpCd(CdDtl langTpCd) {
		this.langTpCd = langTpCd;
	}
	public Long getLangId() {
		return langId;
	}
	public void setLangId(Long langId) {
		this.langId = langId;
	}
	public String getLangTp() {
		return langTp;
	}
	public void setLangTp(String langTp) {
		this.langTp = langTp;
	}
	public String getDispNm() {
		return dispNm;
	}
	public void setDispNm(String dispNm) {
		this.dispNm = dispNm;
	}

}
