package net.jcms.conts.Research.model;


import net.jcms.framework.base.model.BaseModel;

public class ResearchQuest extends BaseModel{
	
	private int qstSeq;
	private int rchSeq;
	private int qstPrint;
	private String qstTp;
	private String qstQury;
	private int qstMin;
	private int qstMax;
	private int qstOrd;
	
	public int getQstSeq() {
		return qstSeq;
	}
	public void setQstSeq(int qstSeq) {
		this.qstSeq = qstSeq;
	}
	public int getRchSeq() {
		return rchSeq;
	}
	public void setRchSeq(int rchSeq) {
		this.rchSeq = rchSeq;
	}
	public int getQstPrint() {
		return qstPrint;
	}
	public void setQstPrint(int qstPrint) {
		this.qstPrint = qstPrint;
	}
	public String getQstTp() {
		return qstTp;
	}
	public void setQstTp(String qstTp) {
		this.qstTp = qstTp;
	}
	public String getQstQury() {
		return qstQury;
	}
	public void setQstQury(String qstQury) {
		this.qstQury = qstQury;
	}
	public int getQstMin() {
		return qstMin;
	}
	public void setQstMin(int qstMin) {
		this.qstMin = qstMin;
	}
	public int getQstMax() {
		return qstMax;
	}
	public void setQstMax(int qstMax) {
		this.qstMax = qstMax;
	}
	public int getQstOrd() {
		return qstOrd;
	}
	public void setQstOrd(int qstOrd) {
		this.qstOrd = qstOrd;
	}
	
	@Override
	public String toString() {
		return "ResearchQuest [qstSeq=" + qstSeq + ", rchSeq=" + rchSeq + ", qstPrint=" + qstPrint + ", qstTp=" + qstTp
				+ ", qstQury=" + qstQury + ", qstMin=" + qstMin + ", qstMax=" + qstMax + ", qstOrd=" + qstOrd + "]";
	}
	
	
}
