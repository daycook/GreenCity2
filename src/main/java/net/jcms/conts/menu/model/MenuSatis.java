package net.jcms.conts.menu.model;

import net.jcms.framework.base.model.BaseModel;
public class MenuSatis extends BaseModel{
	
	private Long siteId; 		//사이트 아이디
	private Long menuId; 		//메뉴 아이디
	private Integer score; 		//점수
	private Integer cnt; 		//건수
	private String regNm; 		//등록 이름
	private String regIp; 		//등록 아이피
	private String opinion; 	//기타 의견
	
	private String satisRegId;	//등록자 아이디
	
	public Long getSiteId() {
		return siteId;
	}
	public void setSiteId(Long siteId) {
		this.siteId = siteId;
	}
	public Long getMenuId() {
		return menuId;
	}
	public void setMenuId(Long menuId) {
		this.menuId = menuId;
	}
	public Integer getScore() {
		return score;
	}
	public void setScore(Integer score) {
		this.score = score;
	}
	public Integer getCnt() {
		return cnt;
	}
	public void setCnt(Integer cnt) {
		this.cnt = cnt;
	}
	public String getRegNm() {
		return regNm;
	}
	public void setRegNm(String regNm) {
		this.regNm = regNm;
	}
	public String getRegIp() {
		return regIp;
	}
	public void setRegIp(String regIp) {
		this.regIp = regIp;
	}
	public String getOpinion() {
		return opinion;
	}
	public void setOpinion(String opinion) {
		this.opinion = opinion;
	}
	public String getSatisRegId() {
		return satisRegId;
	}
	public void setSatisRegId(String satisRegId) {
		this.satisRegId = satisRegId;
	}
}
